var a = 55
// now Javascript allows the variables to be changed at runtime
// here a is the identifier and 55 is called as the literal and = is an assignment operator
console.log(a)
// now we will change the value of the variable a from an integer to a string
a = "Sourav"
console.log(a)
// the value of the variable will get changed and that is why Javascript is known as a Dynamically typed language